# Sorting of array is done by using sort() method
from numpy import *
arr = array([6,5,6,3,2,4,9,0])
s = sort(arr)
print(s,'\n')
arr1 = array(['zigzag','yell','cowboy','lover boy'])
s1 = sort(arr1)
print(s1,'\n')
arr2 = array([[4,6,9,0],[1,5,7,0]])
s2 = sort(arr2)
print(s2,'\n')
arr3 = array(['True','False','False','True'])
s3 = sort(arr3)
print(s3)